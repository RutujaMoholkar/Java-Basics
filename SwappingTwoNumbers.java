class SwappingTwoNumbers{

       public static void main(String[] args){
             
           int a=56;
           int b=78;
           
           System.out.println("Value of a and b before swapping is: "+a+"  "+b);
           int temp=a;
           a=b;
           b=temp;
           
           System.out.println("Value of a and b after swapping is: "+a+"  "+b);
           

}

}

